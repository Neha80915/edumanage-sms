export function calculateAttendancePercentage(studentId: string): number {
  const studentAttendance = mockAttendance.filter(a => a.studentId === studentId);
  const presentDays = studentAttendance.filter(a => a.status === 'Present' || a.status === 'Late').length;
  return studentAttendance.length > 0 ? Math.round((presentDays / studentAttendance.length) * 100) : 0;
}

export function calculateGPA(studentId: string): number {
  const studentMarks = mockMarks.filter(m => m.studentId === studentId);
  if (studentMarks.length === 0) return 0;

  const totalPercentage = studentMarks.reduce((sum, mark) => {
    return sum + (mark.obtainedMarks / mark.maxMarks) * 100;
  }, 0);

  const avgPercentage = totalPercentage / studentMarks.length;
  return Number((avgPercentage / 10).toFixed(2));
}
